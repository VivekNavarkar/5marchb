package loop_6;
public class ForLoops
{
   public static void main(String[] args) 
  {  System.out.println("**To Print Increement 1 to 5 numbers**");
	 for(int i=1;i<=5;i++)//for(initialization;condition;updation=incre/decre)
	 {
		 System.out.println(i);
	 }
	 System.out.println("** Print Increement 5th Table**");
	 for(int i=5;i<=50;i=i+5) 
	 {
		 System.out.println(i);
	 }
	 System.out.println("** Print decrement 6 to 1 numbers ");
	 for(int i=6;i>=1;i--) 
	 {
		 System.out.println(i);
	 }
	 System.out.println("Print decr of 6th table");
	 for(int i=60;i>=6;i=i-6)
	 {
		 System.out.println(i);
	 }
	 System.out.println("**TO Print String**");
	 for(int i=1;i<=5;i++) 
	 {
		 System.out.println("vivek"); 
	 }
  }
	
}
