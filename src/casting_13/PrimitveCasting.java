package casting_13;

public class PrimitveCasting 
{
  public static void main(String[] args) 
  {   
	  //imlicit casting
	int a=5;//
	System.out.println(a);
	double b=a;
	System.out.println(b);
	
	// explicit casting
	double c=10.5;
	System.out.println(c);
	int d=(int)c;
	System.out.println(d);
  }
   


  
}
